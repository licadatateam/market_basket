  node_adjacencies = []
  node_text = []
  node_size = []
  see = []
  for node, adjacencies in enumerate(G.adjacency()):
      confidence = round(pd.DataFrame.from_dict(adjacencies[1]).iloc[:,0].loc['confidence']*100,2)
